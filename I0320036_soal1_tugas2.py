#Mengkonversi Celsius ke Fahrenheit
print ("Hai, kamu yang disana!<3")
print ("˗ˏˋ Mengkonversi Nilai Suhu Celsius ke Fahrenheit ´ˎ˗")
print ("Ayo masukkan derajat suhu dalam celsius!")
Celsius = float(input("Suhu Celsius : "))
#Rumus konversi
Konversi = (9/5*Celsius+32)
print ("°•*⁀➷ Hasil dari konversi suhu adalah", Konversi,"°F")
print ("Sampai jumpa lagi!",'\n')

